                                    <span style="display: block; text-align: justify; font-family: FuturisC, Calibri, sans-serif; font-size: 14px; color: #222222; margin: 10px 0; line-height: 16px;"> <b>Сеялка культиваторного типа типа Maxim II</b> может использоваться для легкой культивации, например, вспаханного поля. Оснащена стойками и пружинной защитой с давлением <b style="color: rgb(211,35,42);">180 кг</b>.<br />
                                     Копирование почвы и прикатывание с большим усилием обеспечивает равномерное прорастание и созревание посевов даже в условиях недостатка влаги. <br />
                                     <b> Сеялка </b>состоит из <b>секций-крыльев</b> , которые полностью независимы друг от друга и <b>бункера</b> для семян и гранулированных удобрений с системой распределения.<br /> 
                                     <b>Отличием</b> сеялки является конструкция рамы, <b>вес</b> которой <b>распределяется</b> между передними сдвоенными колесами и задними прикатывающими катками, что <b>гарантирует отличное прикатывание и контакт семени с почвой.</b></span>
                                    <span style="display: block; text-align: justify; font-family: FuturisC, Calibri, sans-serif; font-size: 14px; color: #222222; margin: 10px 0; line-height: 16px;"><b>Пружина</b> в механизме пружинной защиты, разработанном компанией Моррис, расположена под углом к стойке и соединена с ней через рычаг-коромысло. Этот <b>механизм запатентован.</b> Данная конструкция позволяет сохранять постоянное давление на стойку в рабочем состоянии – <b>180 кг</b>, при встрече стрельчатой лапы с препятствием и подъеме стойки над землей давление на нее сохраняется постоянным. Стойки не требуют обслуживания.<br />
                                    <b>Расстояние между стойками – 25см.</b> <br /> Позволяет соломе свободно проходить, не застревая между стойками, что особенно важно при культивации и посеве по минимальной технологии, когда солома остается на полях. <br /> <b>Универсальный агрегат</b> – может использоваться при традиционной и минимальной обработке поля. Идеально подходит для перехода с традиционной на минимальную обработку почвы. В течение одного сезона выравнивает рельеф полей.<br />
                                    <b>Заменяет Вам несколько сеялок, культиваторов, разбрасывателей.</b> <br /> <b>Позволяет экономить удобрения</b> и увеличивает эффект от их внесения за счет расположения с зерном или в непосредственной близости от него.<br /></span>
                                    <span style="display: block; text-align: justify; font-family: FuturisC, Calibri, sans-serif; font-size: 14px; color: #222222; margin: 10px 0; line-height: 16px;">
                                    Сеялка оборудована раздельной системой внесения семян и удобрений, которая состоит из второго контура труб и распределителей, сошников Gumbo boot для раздельного внесения удобрений, состоящих из твердосплавных сменных наконечников и сменных пластин. С таким сошником сеялка оказывает минимальное воздействие на почву и позволяет закладывать основную дозу минеральных удобрений ниже уровня семян, а семена располагаются в две строчки по разные стороны удобрений. 
