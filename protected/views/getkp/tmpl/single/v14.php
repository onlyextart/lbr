                                    <b>Особенности сошника Gumbo boot:</b><br /><br />
                                    • Семяпроводы расположены сзади сошника, в ряд<br />
                                    • Раздельное внесение семян и удобрений<br />
                                    • Заменямость<br />
                                    • Доп. провод для внесения аммиака и других жидких удобрений<br />
                                    • Размещение позади сошника для долговечности<br />
                                    • Особенно экономично для хозяйств с большими полями – более 50 га.<br /><br />
                                    Позволяет соломе свободно проходить, не застревая между стойками, что особенно важно при культивации и посеве по минимальной технологии, когда солома остается на полях.<br />
                                    <b>Универсальный агрегат</b> – может использоваться при традиционной и минимальной обработке поля. Идеально подходит для перехода с традиционной на минимальную обработку почвы. В течение одного сезона выравнивает рельеф полей.<br />
