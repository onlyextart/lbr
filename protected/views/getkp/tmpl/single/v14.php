                                    <span style="display: block; text-align: justify; font-family: FuturisC, Calibri, sans-serif; font-size: 14px; color: #222222; margin: 10px 0; line-height: 16px;"><b>Система сеялки-культиватора</b> состоит из <b>трех секций-крыльев</b>, которые полностью независимы друг от друга и позволяют идеально копировать рельеф поля и производить посев на одинаковую глубину по всей ширине сеялки..<br />
                                     Копирование почвы и прикатывание с большим усилием обеспечивает равномерное прорастание и созревание посевов даже в условиях недостатка влаги. <br />
                                     <b> Сеялка </b>состоит из <b>секций-крыльев</b> , которые полностью независимы друг от друга и <b>бункера</b> для семян и гранулированных удобрений с системой распределения.<br /> 
                                     <b>Отличием</b> сеялки является конструкция рамы, <b>вес</b> которой <b>распределяется</b> между передними сдвоенными колесами и задними прикатывающими катками, что <b>гарантирует отличное прикатывание и контакт семени с почвой.</b></span>
                                    <span style="display: block; text-align: justify; font-family: FuturisC, Calibri, sans-serif; font-size: 14px; color: #222222; margin: 10px 0; line-height: 16px;"><b>Пружина</b> в механизме пружинной защиты, разработанном компанией Моррис, расположена под углом к стойке и соединена с ней через рычаг-коромысло. <b>Этот механизм запатентован.</b> 
                                    Данная конструкция позволяет сохранять постоянное давление на стойку в рабочем состоянии – 350 кг, при встрече стрельчатой лапы с препятствием и подъеме стойки над землей давление на нее <b>сохраняется постоянным</b>. Стойки не требуют обслуживания.<br />
                                    Один агрегат в комплексе с мощным трактором может обработать за сезон до <b>3000 га </b>в зависимости от ширины при посеве и такую же площадь при культивации. <br /> Скорость движения при посеве составляет <b>8-10 км/ч</b>.<br />
                                    </span>
